package entity;

public class kasir_entity {
    
    private String nama;
    private String username;
    private String password;

    public kasir_entity(String nama, String username, String password) {
        this.nama = nama;
        this.username = username;
        this.password = password;
    }

    public String getNama() {
        return nama;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

 

    
    
}
